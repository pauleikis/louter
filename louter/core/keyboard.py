class PhysicalKeyboard:
    template = None
    size = 0

    strain = []
    fingers = []
    space_finger = t
    space_strain = 1

    def __str__(self):
        return self.__class__.__name__ + '\n' + self.template.format(*[' '] * self.size)

    def __eq__(self, other):
        return type(self) is type(other)

    @property
    def stretch(self):
      return {}


